local function makeRarityStyle(props)
	local strokeThickness = props.strokeThickness or 1
	local glowThickness = props.glowThickness or 0
	local sheenHeight = props.sheenHeight or 0

	return {
		textColor = props.textColor,
		borderColor = props.borderColor,
		glowColor = props.glowColor or props.borderColor,
		gradientStart = props.gradientStart or props.glowColor or props.borderColor,
		gradientEnd = props.gradientEnd or props.borderColor,
		strokeThickness = strokeThickness,
		hoverStrokeThickness = props.hoverStrokeThickness or (strokeThickness + 0.18),
		strokeTransparency = props.strokeTransparency,
		hoverStrokeTransparency = props.hoverStrokeTransparency,
		glowThickness = glowThickness,
		hoverGlowThickness = props.hoverGlowThickness or glowThickness,
		glowTransparency = props.glowTransparency,
		hoverGlowTransparency = props.hoverGlowTransparency,
		sheenHeight = sheenHeight,
		hoverSheenHeight = props.hoverSheenHeight or sheenHeight,
		sheenRotation = props.sheenRotation or 0,
		sheenTopTransparency = props.sheenTopTransparency,
		hoverSheenTopTransparency = props.hoverSheenTopTransparency or props.sheenTopTransparency,
	}
end

